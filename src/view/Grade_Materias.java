    private void seg_attActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seg_attActionPerformed
        // TODO add your handling code here:
        
        if( seg_tb.getSelectedRow() != -1 )
        {
            grade g = new grade();
            
            Crud c = new Crud();
            
            g.setId( Integer.parseInt(seg_id.getText()) );
            g.setHora( seg_h.getText() );
            g.setMateria( seg_m.getText() );
            g.setSala( seg_s.getText() );
            c.update(g, "tb_seg");
            
            seg_id.setText("");
            seg_h.setText("");
            seg_m.setText("");
            
            readTable();
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para atualizar!");
        }
        
    }//GEN-LAST:event_seg_attActionPerformed

    private void seg_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seg_delActionPerformed
        // TODO add your handling code here:
        
        if( seg_tb.getSelectedRow() != -1 )
        {
            
           if( JOptionPane.showConfirmDialog(null, "Deseja deletar o id " + seg_id.getText() + "?") == JOptionPane.YES_OPTION )
           {
               grade g = new grade();
               Crud c = new Crud();
               
               g.setId( Integer.parseInt( seg_id.getText() ) );
               c.delete(g, "tb_seg");
               readTable();
           }
           else
           {
               //JOptionPane.showMessageDialog(null, "Clicou em não!!");
           }
           
        }        
        
    }//GEN-LAST:event_seg_delActionPerformed
