    public void readTable()
    {
        DefaultTableModel modeloSeg = (DefaultTableModel) seg_tb.getModel();
        DefaultTableModel modeloTer = (DefaultTableModel) ter_tb.getModel();
        DefaultTableModel modeloQua = (DefaultTableModel) qua_tb.getModel();
        DefaultTableModel modeloQui = (DefaultTableModel) qui_tb.getModel();
        DefaultTableModel modeloSex = (DefaultTableModel) sex_tb.getModel();
        DefaultTableModel modeloSab = (DefaultTableModel) sab_tb.getModel();
        DefaultTableModel modeloDom = (DefaultTableModel) dom_tb.getModel();
               
        Crud c = new Crud();
        
        DefaultTableModel[] tabelas = {modeloSeg, modeloTer, modeloQua, modeloQui, modeloSex, modeloSab, modeloDom};
        String[] tabelasNomes       = {"tb_seg", "tb_ter",   "tb_qua",  "tb_qui",  "tb_sex",  "tb_sab",  "tb_dom" };
        JTextField[] nomesCampos = 
        {
            seg_id,  ter_id, qua_id, qui_id, sex_id, sab_id, dom_id,            
            seg_s,  ter_s, qua_s, qui_s, sex_s, sab_s, dom_s,
            seg_m,  ter_m, qua_m, qui_m, sex_m, sab_m, dom_m,
            seg_h,  ter_h, qua_h, qui_h, sex_h, sab_h, dom_h,            
        };
        int index = -1;
        try{
            for (DefaultTableModel tabela : tabelas) {
              index++;
              tabela.setNumRows(0);            
              c.read(tabelasNomes[index], cmb_grade.getSelectedItem().toString()).forEach((g) -> {
                  tabela.addRow(new Object[]{
                      g.getId(), g.getSala(), g.getHora(), g.getMateria()
                  });
              });
            }
            //JOptionPane.showMessageDialog(null, "Dados Carregados com sucesso!");
            
            for(JTextField campo : nomesCampos)
            {
                campo.setText("");                
            }
            
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Encerrando Aplicação! -> Erro nas instruções sql : "+ex);
            System.exit(0);
        }
    }
    
    public void lerHora()
    {
        Timer t = new Timer();
        
        t.scheduleAtFixedRate(new TimerTask() {
            @Override
            public void run() {
                
                Calendar c = Calendar.getInstance();
                SimpleDateFormat dia = new SimpleDateFormat("dd");
                SimpleDateFormat mes = new SimpleDateFormat("MMMM");
                SimpleDateFormat ano = new SimpleDateFormat("yyyy");
                SimpleDateFormat hora = new SimpleDateFormat("HH:mm:ss");                
                String data = dia.format(c.getTime())+", "+mes.format(c.getTime())+" de "+ano.format(c.getTime())+". "+hora.format(c.getTime());
                grade_hora.setText( data );
                
            }
        }, 0, 1000);
        


    }
    
